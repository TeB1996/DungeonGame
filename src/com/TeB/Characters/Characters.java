package com.TeB.Characters;

import java.awt.Image;

public class Characters {
	
	protected Image CharacterImage;
	protected int width, height;
	
	public Characters (){
		
	}
	
	public Image getCharacterImage(){
		return CharacterImage;
	}
	
	public int getWidth(){
		return width;
	}
	
	public int getHeight(){
		return height;
	}

}
